#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	int		i;
	char	*s;

	i = 0;
	s = (char*)str;
	while (s[i] != '\0')
		i++;
	while (s[i] != c && i >= 0)
		i--;
	if (i < 0)
		return (NULL);
	else
		return (s + i);
}
