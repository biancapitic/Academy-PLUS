#include "libft.h"

void	ft_putendl(char const *s)
{
	unsigned int	i;
	char			j;

	if (s == NULL)
		return ;
	i = 0;
	j = '\n';
	while (s[i] != '\0')
	{
		write(1, s + i, 1);
		i++;
	}
	write(1, &j, 1);
}
