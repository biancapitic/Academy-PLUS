#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	int		i;
	char	*p;

	i = 0;
	p = (char*)src;
	while (p[i] != '\0')
	{
		dest[i] = p[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
