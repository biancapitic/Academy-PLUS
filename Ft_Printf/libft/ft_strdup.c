#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	int		n;
	char	*str;
	char	*s;

	s = (char*)src;
	n = ft_strlen(s);
	if (!(str = (char*)malloc(sizeof(*s) * (n + 1))))
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
