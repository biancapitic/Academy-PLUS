#include "libft.h"

void	ft_putstr_fd(char const *s, int fd)
{
	char	*p;
	int		i;

	if (!s)
		return ;
	p = (char*)s;
	i = 0;
	while (p[i] != '\0')
	{
		ft_putchar_fd(p[i], fd);
		i++;
	}
}
