#include "libft.h"

int		ft_atoi(const char *str)
{
	int nr;
	int i;
	int s;

	nr = 0;
	i = 0;
	s = 1;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s = -1;
		i++;
	}
	while (str[i] == '0')
		i++;
	while (str[i] >= 48 && str[i] <= 57)
	{
		nr = nr * 10 + str[i] - 48;
		i++;
	}
	return (nr * s);
}
